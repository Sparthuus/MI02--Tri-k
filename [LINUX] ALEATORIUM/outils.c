void verifchar(char *nom, int max) {
    /* V√©rifier pointeur nom */
    if (nom == NULL) {
        printf("Le pointeur nom est NULL !\n");
        exit(EXIT_FAILURE);
    }

    printf("\n+----------------------[Tapez votre r√©ponse]-------------------+\n");

    do {
        fgets(nom, max + 1, stdin);

        // Supprimer le '\n' de la fin de la cha√Æne s'il est pr√©sent
        if (nom[strlen(nom) - 1] == '\n') {
            nom[strlen(nom) - 1] = '\0';
        } else {
            // Si la cha√Æne est trop longue, vider le reste du flux d'entr√©e
            int c;
            while ((c = getchar()) != '\n' && c != EOF) {}
        }

        // V√©rifier si la saisie est juste une ligne vide
        if (strlen(nom) == 0 || (strlen(nom) == 1 && nom[0] == '\n')) {
            printf("Saisie invalide. Veuillez entrer quelque chose.\n");
        }
        
    } while (strlen(nom) == 0 || (strlen(nom) == 1 && nom[0] == '\n') || strlen(nom) > max); // Continuer tant que la saisie est invalide ou d√©passe la limite
}
